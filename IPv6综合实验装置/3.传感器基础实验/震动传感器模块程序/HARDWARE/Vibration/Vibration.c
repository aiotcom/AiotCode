#include "delay.h"
#include "Vibration.h"

//==========================================================
//	函数名称：	Vibration_Init()
//
//	函数功能：	震动传感器初始化
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：		
//==========================================================
void Vibration_Init(void)
{
    P1SEL &= ~0x03;     //设置P1.0、P1.1为普通IO口  
    P1DIR &= ~0x03;     //按键接在P1.0、P1.1口上，设P1.0、P1.1为输入模式 
    P1INP &= ~0x03;     //打开P1.0、P1.1上拉电阻
}
