#include <ioCC2530.h>
#include "delay.h"
#include "IWDG.h"
#include "Lamp.h"
#include "KEY.h"
#include "LED.h"

//==========================================================
//	函数名称：	main()
//
//	函数功能：	程序入口函数
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：		
//==========================================================
void main(void)
{
    Hal_Init_32M();   //初始化32M时钟
    LED_Init();       //LED初始化
    KEY_Init();       //按键初始化
    LampInit();       //底座灯初始化
    Init_Watchdog();  //看门狗初始化(1ms)
    LED_OFF();
    delay_ms(500);
    
    while(1)
    {
      if(KEY_Scan(0)) //有按键按下
      {
        FeetDog();  //看门狗喂狗  
      }
      LED_ON();
    }
}
