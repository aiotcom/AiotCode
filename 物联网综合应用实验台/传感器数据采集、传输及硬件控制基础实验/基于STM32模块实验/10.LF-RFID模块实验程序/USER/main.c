uint8_t CardID[5];

int main(void)
{
	HAL_Init();       		//初始化HAL库   
	Rs485_Init();					//初始化485
	EM4095_Init();				//初始化EM4095
	UART7_Init(115200);		//初始化串口七
	TIM2_Init(2000-1,108-1);				//初始化定时器2(2ms中断)

	while(1)
	{
		if(EM4095_SearchHdr(CardID) == GET_ID)
		{
				Rs485_Send(CardID,4);			//发送ID数据
		}
	}
}



