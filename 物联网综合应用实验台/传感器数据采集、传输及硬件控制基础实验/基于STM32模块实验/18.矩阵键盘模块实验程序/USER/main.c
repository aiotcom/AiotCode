{
	HAL_Init();       //初始化HAL库   
	KEY_Init();				//初始化按键
	Rs485_Init();			//初始化485
	UART7_Init(115200);		//初始化串口七

	while(1)
	{
		KEY_ConFig();  	//接收按键输入
		Key_Feedback();	//检测到按键后打印到串口中
	}
}







