#include "delay.h"
#include "Relay.h"

//==========================================================
//	函数名称：	Relay_Init()
//
//	函数功能：	初始化继电器
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：		
//==========================================================
void Relay_Init(void)
{
   P0DIR |= 0x0C;      //P0.2、P0.3定义为输出
   Relay1_OFF();Relay2_OFF();
}