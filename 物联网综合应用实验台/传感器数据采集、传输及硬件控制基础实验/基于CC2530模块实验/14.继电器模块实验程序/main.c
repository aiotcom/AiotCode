#include <ioCC2530.h>
#include "Relay.h"
#include "delay.h"
#include "Lamp.h"

//==========================================================
//	函数名称：	main()
//
//	函数功能：	程序入口函数
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：		LED模块程序
//==========================================================
void main(void)
{
    Hal_Init_32M();   //初始化32M时钟
    LampInit();       //初始化底座灯
    Relay_Init();     //初始化继电器

    while(1)
    {
       Relay1_ON();
       delay_ms(5000);
       Relay1_OFF();
       delay_ms(5000);
       Relay2_ON();
       delay_ms(5000);
       Relay2_OFF();
       delay_ms(5000);
    }
}
