int main(void)
{
	HAL_Init();       //初始化HAL库
	LED_Init();				//初始化LED
	KEY_Init();				//初始化按键
	IWDG_Init(IWDG_PRESCALER_64,500); 	//分频数为64,重载值为500,溢出时间为1s
	delay_ms(500);

	while(1)
	{
		LED_ON();					//四个LED全部保持常亮
		if(KEY_Scan(1))  	//如果有任意按键按下，执行喂狗操作
		{
			IWDG_Feed();  	//喂狗
		}
	}
}



