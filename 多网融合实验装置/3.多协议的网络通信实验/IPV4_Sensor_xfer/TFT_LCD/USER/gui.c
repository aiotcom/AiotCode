void GUI_DrawSymbol(uint16_t x0,uint16_t y0,uint8_t H,uint8_t W,const uint8_t *pSymbol)
{
	uint8_t byte,zByte,yBit,NumBitPerByte,NumBytePerCol;
	uint16_t xidx,yidx,bidx;
	
	zByte         = W/8;    
	yBit	      = W%8;
	NumBytePerCol = zByte + (yBit?1:0);
	
	for(yidx = 0;yidx < H;yidx++)//
	{
		for(bidx = 0;bidx < NumBytePerCol;bidx++)//
		{
			byte          = pSymbol[yidx*NumBytePerCol+bidx];//
			NumBitPerByte = (bidx == zByte)?yBit:8;
			
			for(xidx = 0;xidx < NumBitPerByte;xidx++)//
			{
				if(byte&0x80)
				{
					//LCD_DrawPoint(x0+bidx*8+xidx,y0+yidx);
                    Address_set(x0+bidx*8+xidx,y0+yidx,x0+bidx*8+xidx,y0+yidx);//设置光标位置 
                    LCD_WR_DATA(POINT_COLOR); 	
				}
                else
                {
                     Address_set(x0+bidx*8+xidx,y0+yidx,x0+bidx*8+xidx,y0+yidx);//设置光标位置 
                    LCD_WR_DATA(BACK_COLOR);                
                }
				byte = byte << 1;
			}
		}
	}
}
