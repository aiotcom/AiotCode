void GUI_DrawSymbolString_H32xW16(uint16_t x0,uint16_t y0,uint8_t *pstring)
{
    BACK_COLOR = BACKGROUND_COLOUR;
    while(*pstring)
    {
        c = *pstring;
        if(('0' <= c) && (c <= '9'))
        {
            index1 = c - 0x30;
            GUI_DrawSymbol(x0,y0,32,16,&Font_English_H32xW16[index1][0]);
        }
        else if(c == 'N')
        {//N
            GUI_DrawSymbol(x0,y0,32,16,&Font_English_H32xW16[11][0]);
        }
        else if(c == 'C')
        {//N
            GUI_DrawTempSymbol(x0,y0);
        } 
        else if(c == '%')
        {
            GUI_DrawSymbol(x0,y0,32,16,&Font_English_H32xW16[10][0]);        
        } 
        else if(c == ' ')
        {
            GUI_DrawSymbol(x0,y0,32,16,&Font_English_H32xW16[12][0]);        
        } 
        else if(c == 'h')
        {
            GUI_DrawSymbol(x0,y0,32,16,&Font_English_H32xW16[13][0]); 
        } 
        else if(c == 'P')
        {
            GUI_DrawSymbol(x0,y0,32,16,&Font_English_H32xW16[14][0]);        
        } 
        else if(c == 'a')
        {
            GUI_DrawSymbol(x0,y0,32,16,&Font_English_H32xW16[15][0]);        
        }  
        else if(c == '.')
        {
            GUI_DrawSymbol(x0,y0,32,16,&Font_English_H32xW16[16][0]);        
        }          
        pstring++;
        x0 = x0 + 16; 
    }
}
