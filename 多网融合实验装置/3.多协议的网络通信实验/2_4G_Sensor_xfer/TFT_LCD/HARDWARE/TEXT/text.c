//在指定位置，显示一个24*24大小的汉字
//x,y :汉字的坐标
//index:汉字编号（在字库数组里面的编号） 
void LCD_Show_Font(unsigned int x,unsigned int y,unsigned char index)
{
	u8 temp,t,t1;
	u16 y0=y;
	u8 *dzk;   
	u8 csize=72;					//一个24*24的汉字72字节
	dzk=(u8*)LCD_HZK_TBL[index];	//得到汉字编号对应的点阵库 
	for(t=0;t<csize;t++)
	{   												   
		temp=dzk[t];				//得到点阵数据                          
		for(t1=0;t1<8;t1++)			//按照从高位到低位的顺序画点
		{
			//if(temp&0x80)LCD_Fast_DrawPoint(x,y,POINT_COLOR);
			if(temp&0x80)LCD_one_DrawPoint(x,y,POINT_COLOR);
			else LCD_one_DrawPoint(x,y,BACK_COLOR); 
			temp<<=1;
			y++;
			if((y-y0)==24)			//对y坐标的处理，当y坐标距起始坐标差24个像素点，x坐标加1
			{
				y=y0;
				x++;
				break;
			}
		}  	 
	}  
}

void LCD_one_DrawPoint(unsigned int x,unsigned int y,unsigned int COLOR)
{
		Address_set(x,y,x,y);//设置光标位置 
	 LCD_WR_DATA(COLOR); 	
}















