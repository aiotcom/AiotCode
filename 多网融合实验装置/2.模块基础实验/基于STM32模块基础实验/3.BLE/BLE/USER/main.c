int main(void)
{
    HAL_Init();             					//初始化HAL库  
	UART2_Init(9600);									//初始化串口2
	BLE_Init(0);											//初始化蓝牙
	TIM2_Init(1000-1,64-1);						//初始化定时器2(1ms)

	while(1)
	{
		USART2_IRQHand();								//串口2数据接收处理(数据来自手机)
	}
}






